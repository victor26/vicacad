<h1>FLEXÃO DE PERNAS EM PÉ</h1>

<h2>EXECUÇÃO</h2>
Para executar a Flexão de pernas em pé:<br>
<ol>
    <li>Coloque um calcanhar por baixo do rolo almofadado e apoie o peso com a outra perna.</l1>
        <li>Levante o peso dobrando o joelho e elevando o calcanhar na direção da nádega.</l1>
            <li>Abaixe o peso de volta a posição inicial.</l1>
                </ol>
