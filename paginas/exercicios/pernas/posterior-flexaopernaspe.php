<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Músculos posteriores da coxa.
Secundário: Glúteos, músculos da panturrilha.</pre>

<img src="../imagens/pernas/flexão-de-pernas-em-pé.jpg"/>

<h2>POSIÇÃO DO CORPO</h2>
<p>
A superfície acolchoada da maioria dos aparelhos tem uma angulação no nível do quadril, o que dobra ligeiramente o torso para frente.
Essa posição do corpo inclina a pelve e alonga os músculos posteriores da coxa, o que ajuda no isolamento dos músculos.
Dependendo do modelo do aparelho, a perna de apoio pode assumir uma posição de pé ou ajoelhada. (Ver seção de variação).</p>

<h2>AMPLITUDE DE MOVIMENTO</h2>
<p>
Dobre os joelhos o máximo possível durante a fase de subida. Interrompa o movimento alguns graus antes da extensão completa na parte baixa do exercício; dessa forma, será mantida a tensão nos músculos posteriores da coxa, sendo ainda minimizada a tensão na articulação do joelho.</p>

<h2>RESISTÊNCIA</h2>
<p>
Ao contrário da flexão de perna deitado, a flexão de perna em pé é executada com uma de cada vez, o que favorece o isolamento do músculo e o enfoque.
A resistência é razoavelmente uniforme, mas em muitas das maquinas mais modernas é mais baixa na posição inicial, em que os músculos posteriores da coxa estão completamente alongados – e mais vulneráveis a lesões.</p>

