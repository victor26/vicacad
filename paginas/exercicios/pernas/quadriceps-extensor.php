<h1>EXTENSÃO DE PERNAS</h1>

<h2>EXECUÇÃO</h2>
Para executar a Extensão de Pernas:<br>
<ol>
    <li>Sente-se no aparelho e coloque os pés por baixo dos rolos.</l1>
        <li>Levante as pernas para cima, até que os joelhos estejam estendidos.</l1>
            <li>Abaixe as pernas de volta à posição inicial, com os joelhos dobrados em 90 graus.</l1>
                </ol>
<img src="../gifs/pernas/extensaopernas.gif"/>
	
	

<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Quadríceps
Secundário: Tibial anterior</pre>

<img src="../imagens/pernas/extensão-de-pernas.jpg"/>


<h2>ESPAÇAMENTO DOS PÉS</h2>
<p>
Não há muito espaço nos rolos para ajuste do espaçamento dos pés, mas a colocação dos pés juntos tenderá a mobilizar o quadríceps externo e um espaçamento maior enfatizará um pouco mais o quadríceps interno</p>

<h2>POSIÇÃO DO CORPO</h2>
<p>
Ajuste o encosto de modo que a parte posterior do joelho se acomode confortavelmente contra a borda do assento, fazendo com que a coxa toda fique apoiada. A inclinação do torso para trás ou a elevação das nádegas para fora do assento estenderá a articulação do quadril, alongando o reto femoral; dessa forma, essa seção do quadríceps trabalhará mais puxado durante o exercício.</p>

<h2>AMPLITUDE DE MOVIMENTO</h2>
<p>
Ao realizar o Extensão de pernas, o arco de movimento deve ser de aproximadamente 90 graus. Contraia vigorosamente o quadríceps na parte alta, quando os joelhos estiverem completamente esticados. Para evitar excessiva tensão na patela (rótula), não dobre os joelhos além dos 90 graus.</p>

<h2>RESISTÊNCIA</h2>
<p>
A resistência é razoavelmente uniforme, mas em muitos dos aparelhos modernos a resistência aumenta ligeiramente com o levantamento do peso. Menos resistência na posição inicial minimizará a tensão através da patela, com o joelho na posição dobrada.</p>

