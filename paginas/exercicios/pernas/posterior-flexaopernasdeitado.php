<h1>FLEXÃO DE PERNAS DEITADO</h1>

<h2>EXECUÇÃO</h2>
Para executar a Flexão de pernas deitado:<br>
<ol>
    <li>Deite-se de bruços sobre o aparelho e enganche os calcanhares por baixo dos rolos almofadados.</l1>
        <li>Levante o peso dobrando os joelhos e eleve os calcanhares na direção das nádegas.</l1>
            <li>Abaixe o peso de volta à posição inicial.</l1>
                </ol>
