<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Músculos posteriores da coxa.
Secundário: Glúteos, músculos da panturrilha.</pre>

<img src="../imagens/pernas/flexão-de-pernas-deitado.jpg"/>

<h2>POSIÇÃO DO CORPO</h2>
<p>
A superfície acolchoada da maioria dos aparelhos tem uma angulação no nível do quadril, o que dobra ligeiramente o torso para frente. Essa posição do corpo inclina a pelve e alonga ligeiramente o torso para frente.</p>

<h2>AMPLITUDE DE MOVIMENTO</h2>
<p>
Dobre os joelhos o máximo possível durante a fase de subida. Interrompa o movimento alguns graus antes da extensão completa na parte baixa do exercício; dessa forma, é mantida a tensão nos músculos posteriores da coxa, sendo ainda minimizada a tensão na articulação do joelho.</p>

<h2>RESISTÊNCIA</h2>
<p>
A resistência é razoavelmente uniforme, mas em muitas das máquinas mais modernas ela é mais baixa na posição inicial, em que os músculos posteriores da coxa estão completamente alongados – e mais vulneráveis a lesões.</p>

