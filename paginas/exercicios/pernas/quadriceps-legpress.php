<h1>LEG PRESS</h1>



<h2>EXECUÇÃO</h2>
Para executar o Leg Press:<br>
<ol>
    <li>Sente-se no aparelho de leg press e coloque os pés com afastamento na plataforma igual à largura dos ombros.</l1>
        <li>Lentamente, abaixe o peso até que os joelhos estejam com 90 graus de flexão.</l1>
            <li>Empurre o peso de volta à posição inicial; para isso, estenda as pernas.</l1>
                </ol>
<img src="../gifs/pernas/legpress.gif"/>
	
	

<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Quadríceps
Secundário: Glúteos, músculos posteriores da coxa, adutores.</pre>

<img src="../imagens/pernas/leg-press.jpg"/>


<h2>TRAGETÓRIA</h2>
<p>
Se você usar as bolas dos pés para levantar os pesos, permitindo que os calcanhares se elevem e se afastem da plataforma enquanto o peso for abaixado, serão mobilizados os quadríceps, com redução da carga nas patelas. Por outro lado, se você utilizar os calcanhares para levantar os pesos, serão enfatizados os músculos posteriores da coxa e glúteos.</p>

<h2>POSIÇÃO DO CORPO</h2>
<p>
O ângulo do torso com as pernas influencia o enfoque muscular e o grau de tensão ao longo da região lombar. Quando o ângulo entre o assento e o encosto está em 90 graus,  enfatiza-se os glúteos e os músculos posteriores da coxa, mas esse ângulo agudo impõe maior esforço na região lombar.
Se o encosto for inclinado para uma posição mais baixa na direção do chão, o torso se inclinará para trás. Essa posição representa menor tensão na coluna vertebral lombar, enfatizando mais os quadríceps.</p>

<h2>AMPLITUDE DE MOVIMENTO</h2>
<p>
Se o movimento for interrompido alguns graus antes do completo bloqueio na parte alta do exercício, os quadríceps permanecerão sob tensão.</p>

<h2>RESISTÊNCIA</h2>
<p>
Em comparação com o agachamento, o leg press reduz a carga axial incidente na coluna vertebral e diminui o riso de dor nas costas. Além disso, o leg press enfatiza o quadríceps, não os glúteos.</p>

