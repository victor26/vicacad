<h1>PANTURRILHA EM PÉ</h1>

<h2>EXECUÇÃO</h2>
Para executar a panturrilha em pé:<br>
<ol>
    <li>Fique em pé com os dedos dos pés sobre a plataforma e os ombros por baixo das almofadas; abaixe os calcanhares o máximo possível, para obtenção de um alongamento completo.</l1>
        <li>Levante o peso elevando os calcanhares até onde for possível, mantendo as pernas estendidas.</l1>
            <li>Abaixe lentamente os calcanhares, de volta à posição inicial.</l1>
                </ol>
<img src="../gifs/pernas/panturrilhape.gif"/>
	
	

<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Gastrocnêmio.
Secundário: Solear.</pre>

<img src="../imagens/pernas/panturrilha-em-pé.jpg"/>

<h2>POSIÇÃO DO CORPO</h2>
<p>
Mantenha os joelhos estendidos e as costas retas. A manutenção dos joelhos estendidos e bloqueados alonga o gastrocnêmio, o que ajuda a concentrar o esforço nesse musculo e minimiza a ação do solear. Se os joelhos dobrarem, isso permitirá que o solear contribua para o movimento.</p>

<h2>AMPLITUDE DE MOVIMENTO</h2>
<p>
Para maximizar a amplitude de movimento, busque fazer um alongamento máximo na parte baixa do exercício e contração máxima na parte alta.</p>

