<h1>AGACHAMENTO HACK</h1>

<h2>EXECUÇÃO</h2>
Para executar o Agachamento Hack:<br>
<ol>
    <li>Posicione as costas contra o encosto e os ombros por baixo dos rolos almofadados e fique em pé com os pés afastados na largura dos ombros sobre a plataforma, com os dedos apontando para frente.</l1>
        <li>Abaixe lentamente o peso, flexionando os joelhos até 90 graus.</l1>
            <li>Empurre o peso de volta à posição inicial, estendendo as pernas.</l1>
                </ol>
<img src="../gifs/pernas/agachamentohack.gif"/>
	
	

<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Quadríceps
Secundário: Glúteos, músculos posteriores da coxa, adutores.</pre>

<img src="../imagens/pernas/agachamento-hack.jpg"/>


<h2>POSIÇÃO DOS PÉS</h2>
<p>
Os dedos devem estar apontando na mesma direção da coxa e do joelho: para frente ou ligeiramente para fora. O posicionamento dos pés baixos na plataforma (perto do corpo) enfatizará o quadríceps, enquanto o posicionamento dos pés em uma posição mais alta na plataforma implicará maior esforço dos glúteos e o músculos posteriores da coxa.</p>

<h2>POSIÇÃO DO CORPO</h2>
<p>
Mantenha a coluna vertebral bem junto ao encosto.</p>

<h2>TRAGETÓRIA</h2>
<p>
Se você usar as bolas dos pés para levantar os pesos, permitindo que os calcanhares se elevem e se afastem da plataforma enquanto o peso for abaixado, ajudará no isolamento dos quadríceps, com redução da carga nas patelas.</p>

<h2>AMPLITUDE DE MOVIMENTO</h2>
<p>
Se o movimento for interrompido alguns graus antes do completo bloqueio na parte alta do exercício, os quadríceps permanecerão sob tensão.</p>

<h2>RESISTÊNCIA</h2>
<p>
Em comparação com o agachamento livre, o encosto no agachamento hack dá sustentação para a coluna vertebral. Além disso, o agachamento hack enfatiza mais os quadríceps e menos os glúteos.</p>

