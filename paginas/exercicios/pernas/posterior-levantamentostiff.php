<h1>LEVANTAMENTO STIFF</h1>

<h2>EXECUÇÃO</h2>
Para executar o Levantamento Stiff:<br>
<ol>
    <li>Fique em pé e mantenha o corpo ereto, com os pés diretamente abaixo dos quadris, segurando uma barra com os braços estendidos.</l1>
        <li>Incline-se para a frente (use a cintura) abaixando o peso, mas mantendo as pernas estendidas.</l1>
            <li>Pare antes que o peso toque o chão e volte a levantá-lo.</l1>
                </ol>
<img src="../gifs/pernas/levantamentostiff.gif"/>
	
	

<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Músculos posteriores da coxa, glúteos.
Secundário: Eretores da espinha, quadríceps.</pre>

<img src="../imagens/pernas/levantamento-Stiff.jpg"/>

<h2>PEGADA</h2>
<p>
As mãos devem ficar com um espaçamento igual à largura dos ombros, de modo que os braços fiquem pendentes na vertical e as mãos avancem ao longo da parte externa das coxas. Uma pegada invertida impedirá a rolagem da barra.</p>

<h2>POSIÇÃO DOS PÉS</h2>
<p>
No Levantamento Stiff, aponte os dedos diretamente abaixo dos quadris.</p>

<h2>POSIÇÃO DO CORPO</h2>
<p>
Os joelhos podem estar ligeiramente dobrados, mas devem ser mantidos estendidos, para que os músculos posteriores da coxa sejam isolados. Mantenha as costas retas durante todo o movimento. A execução desse exercício com as bolas dos dois pés sobre um disco de peso de meia polegada (1,3 cm) é um modo seguro de pré-alongar os músculos posteriores da coxa.</p>

<h2>TRAGETÓRIA</h2>
<p>
No Levantamento Stiff, a barra deve se deslocar diretamente para cima e para baixo, junto ao corpo.</p>

<h2>AMPLITUDE DE MOVIMENTO</h2>
<p>
Abaixe o peso até que os músculos posteriores da coxa atinjam o alongamento completo, sem que ocorra arqueamento da coluna vertebral. Não há necessidade de executar esse exercício em pé sobre um banco ou bloco (como artifício para aumentar a amplitude de movimento). Quando a pelve ficar completamente inclinada para frente, os músculos posteriores da coxa estarão em posição de alongamento máximo.
O arqueamento da coluna vertebral na região lombar não terá qualquer efeito nos músculos posteriores da coxa nem aumentará a amplitude de movimento para baixo. O arqueamento da coluna vertebral lombar meramente aumentará o risco de ocorrência de lesão. Dependendo de sua flexibilidade, a barra deverá ser abaixada até um ponto abaixo dos seus joelhos, ou imediatamente acima dos tornozelos.</p>

<h2>RESISTÊNCIA</h2>
<p>
O levantamento terra com pernas estendidas para os músculos posteriores da coxa exige o uso de um peso menor do que o utilizado durante o tradicional levantamento básico para fortalecimento da região lombar.</p>

