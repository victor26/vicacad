<h1>PANTURRILHA SENTADO</h1>

<h2>EXECUÇÃO</h2>
Para executar a panturrilha sentado:<br>
<ol>
    <li>Coloque as bolas dos pés na plataforma, posicione as almofadas transversalmente à parte inferior das coxas e abaixe os calcanhares o máximo possível.</l1>
        <li>Levante o peso elevando os calcanhares até o ponto mais elevado possível.</l1>
            <li>Abaixe lentamente os calcanhares até a posição inicial.</l1>
                </ol>
<img src="../gifs/pernas/panturrilhasentado.gif.gif"/>
	
	

<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Solear
Secundário: Gastrocnêmio</pre>

<img src="../imagens/pernas/panturrilha-sentado.jpg.jpg"/>

<h2>ESPAÇAMENTO DOS PÉS</h2>
<p>
O posicionamento dos pés com afastamento igual à largura dos quadris trabalhará o gastrocnêmio inteiro. Uma postura aberta tende a enfatizar a cabeça interna (medial) enquanto uma postura fechada trabalha a cabeça externa (lateral).</p>

<h2>POSIÇÃO DO CORPO</h2>
<p>
Posicione a almofada imediatamente acima dos joelhos, não alto demais nas coxas. Na posição sentada, os joelhos dobrados enfatizam tanto o solear como o gastrocnêmio.</p>

<h2>AMPLITUDE DE MOVIMENTO</h2>
<p>
Para maximizar a amplitude de movimento, procure fazer um alongamento máximo na parte baixa do exercício e contração máxima na parte alta.</p>

