<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Peitoral maior superior
Secundários: Deltóide anterior, tríceps.</pre>

<img src="../imagens/peitorais/supino-inclinado-com-barra.jpg"/>

<h2>ESPAÇAMENTO DAS MÃOS</h2>
<p>
Uma pegada com afastamento igual à largura dos ombros ou ligeiramente maior irá mobilizar todas as áreas do músculo peitoral superior.
Um espaçamento mais estreito das mãos enfatiza a parte central interna do tórax, exigindo mais esforço dos tríceps.
Pegadas mais abertas proporcionam maior alongamento com orientação para a parte externa do músculo, e minimizam a contribuição do tríceps; mas com o aumento do espaçamento das mãos, também aumenta o risco de lesão.</p>

