<h1>SUPINO INCLINADO</h1>



<h2>EXECUÇÃO</h2>
Para executar o Supino inclinado:<br>
<ol>
    <li>Na posição sentada em um banco inclinado, faça uma pegada na barra com as palmas das mãos voltadas para cima e com afastamento na mesma distância dos ombros. </l1>
        <li>Abaixe lentamente o peso, até que a barra toque a parte superior do tórax.</l1>
            <li>Empurre a barra diretamente para cima, até que os cotovelos fiquem estendidos.</l1>
                </ol>
