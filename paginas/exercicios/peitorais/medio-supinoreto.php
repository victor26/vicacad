<h1>SUPINO RETO</h1>



<h2>EXECUÇÃO</h2>
Para executar o Supino Reto:<br>
<ol>
    <li>Na posição deitada em um banco plano, faça uma pegada na barra com o dorso das mãos voltado para cima e o afastamento entre elas igual à distância entre os ombros.</l1>
        <li>Abaixe lentamente o peso até tocar a parte média do tórax.</l1>
            <li>Empurre a barra diretamente para cima, até que ocorra bloqueio dos cotovelos.</l1>
                </ol>
