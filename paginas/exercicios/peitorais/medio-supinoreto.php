<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Peitoral maior.
Secundários: Deltóide anterior, tríceps.</pre>

<img src="../imagens/peitorais/supino-reto.jpg"/>

<h2>ESPAÇAMENTO DAS MÃOS</h2>
<p>
O espaçamento ideal das mãos é igual à largura dos ombros, ou ligeiramente maior. Uma pegada fechada (mãos mais próximas) enfatiza os peitorais internos e também mobiliza o tríceps.
Pegadas mais abertas mobilizam a seção externa do músculo, minimizando a contribuição do triceps.</p>

