<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Peitoral maior inferior.
Secundários: Tríceps, deltóide anterior.</pre>

<img src="../imagens/peitorais/supino-declinado.jpg"/>

<h2>ESPAÇAMENTO DAS MÃOS</h2>
<p>
O espaçamento ideal das mãos é o igual à distância entre os ombros. Pegadas mais abertas enfocam a seção externa do músculo, permitem maior alongamento e minimizam a contribuição do tríceps. Uma pegada fechada (mãos mais próxima) enfoca os peitorais internos, exigindo maior trabalho do tríceps.</p>

