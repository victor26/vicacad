<h1>SUPINO DECLINADO</h1>



<h2>EXECUÇÃO</h2>
Para executar o Supino declinado:<br>
<ol>
    <li>Deite-se em um banco declinado e faça uma pegada na barra com o dorso das mãos voltado para cima e com afastamento igual à distância entre os ombros.</l1>
        <li>Abaixe lentamente o peso até tocar a parte inferior do tórax.</l1>
            <li>Empure a barra diretamente para cima, até que ocorra extensão total dos cotovelos</l1>
                </ol>
