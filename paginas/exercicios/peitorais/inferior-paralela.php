<h1>PARALELA</h1>



<h2>EXECUÇÃO</h2>
Para executar a Paralela:<br>
<ol>
    <li>Agarre as barras paralelas, sustentando o corpo com os cotovelos estendidos e bloqueados.</l1>
        <li>Dobre os cotovelos, baixando o torso até que os braços fiquem paralelos ao chão.</l1>
            <li>Empurre o corpo de volta a posição inicial, isto é, até que seus cotovelos fiquem novamente estendidos.</l1>
                </ol>
