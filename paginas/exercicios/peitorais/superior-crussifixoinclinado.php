<h1>CRUCIFIXO INCLINADO</h1>



<h2>EXECUÇÃO</h2>
Para executar o Crucifixo Inclinado:<br>
<ol>
    <li>Sentado em um banco inclinado, comece com os halteres diretamente acima do tórax, com as palmas das mãos voltadas para dentro.</l1>
        <li>Abaixe os halteres para fora, dobrando ligeiramente os cotovelos enquanto os pesos descem até o nível do tórax.</l1>
            <li>Levante os halteres de volta, unindo-os na parte superior do exercício.</l1>
                </ol>
