<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Peitoral maior superior.
Secundários: Deltóide Anterior.</pre>

<img src="../imagens/peitorais/crucifixo-inclinado.jpg"/>

<h2>ESPAÇAMENTO DAS MÃOS</h2>
<p>
A orientação dos halteres afeta a posição da mão. O crucifixo funciona melhor quando os halteres são seguros com uma pegada neutra, mas uma pegada pronada também pode ser utilizada, como variação.</p>

