<h1>SUPINO RETO – HALTER</h1>



<h2>EXECUÇÃO</h2>
Para executar o Supino Reto Com Halteres:<br>
<ol>
    <li>NDeitado em um banco horizontal, comece com os halteres no nível do tórax, palmas das mãos voltadas para a frente.</l1>
        <li>Impulsione verticalmente os halteres, até que ocorra a extensão total dos cotovelos.</l1>
            <li>Abaixe os halteres, retornando-os até a parte média do tórax.</l1>
                </ol>
<img src="../gifs/peitorais/supinoretohalteres.gif"/>
	
	

<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Peitoral maior
Secundários: Deltóide anterior, tríceps.</pre>

<img src="../imagens/peitorais/supino-reto-com-halter.jpg"/>

<h2>AMPLITUDE DE MOVIMENTO</h2>
<p>
Uma repetição mais curta, em que o movimento termina imediatamente antes do bloqueio dos cotovelos, mantém a tensão nos peitorais e reduz a assitência do tríceps. Quanto mais baixo descerem os halteres, maior será o alongamento dos músculos peitorais. Mas se os halteres abaixarem demasiadamente, poderá ocorrer lesão no ombro; é mais seguro terminar a descida quando os halteres chegarem no nível do tórax.</p>

<h2>SUPINO RETO COM GIRO</h2>
<p>
Segure os halteres com uma pegada pronada no início; gire os halteres durante o movimento, de modo que as palmas das mãos fiquem voltadas para dentro por ocasião da extensão total dos cotovelos.</p>


