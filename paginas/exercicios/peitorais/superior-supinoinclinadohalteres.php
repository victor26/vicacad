<h1>SUPINO INCLINADO COM HALTER</h1>



<h2>EXECUÇÃO</h2>
Para executar o Supino inclinado Com halteres:<br>
<ol>
    <li>Na posição sentada em um banco inclinado, comece com os halteres fixos no nível do tórax, com as palmas das mãos voltadas para a frente.</l1>
        <li>Impulsione os halteres verticalmente, até que ocorra bloqueio dos cotovelos.</l1>
            <li>Abaixe os halteres retornando à parte superior do tórax.</l1>
                </ol>
<img src="../gifs/peitorais/supinoinclinadohalteres.gif"/>
	
	

<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Peitoral maior superior
Secundários: Deltóide anterior, tríceps.</pre>

<img src="../imagens/peitorais/supino-inclinado-com-halter.jpg"/>

<h2>ESPAÇAMENTO DAS MÃOS</h2>
<p>
A orientação dos halteres afeta a posição das mãos. A pegada pronada permite maior alongamento enquanto o peso é baixado até a posição inicial.
Uma pegada neutra gera melhor contração na posição de bloqueio dos cotovelos.</p>

