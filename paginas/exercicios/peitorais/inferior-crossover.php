<h1>CROSS OVER</h1>



<h2>EXECUÇÃO</h2>
Para executar o Cross Over:<br>
<ol>
    <li>Na posição em pé, segure os pegadores presos às polias altas de um aparelho de cabos.</l1>
        <li>Tracione simultaneamente para baixo os pegadores, até que as mãos se toquem a frente da cintura; mantenha os cotovelos ligeiramente dobrados.</l1>
            <li>Lentamente, retorne à posição inicial com as mãos no nível dos ombros.</l1>
                </ol>
<img src="../gifs/peitorais/crossover.gif"/>
	
	

<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Peitoral maior inferior (cabeça esternal)
Secundários: Deltóide anterior, tríceps.</pre>

<img src="../imagens/peitorais/cross-over.jpg"/>

<h2>AMPLITUDE DE MOVIMENTO</h2>
<p>
O cruzamento das mãos na parte mais baixa do exercício aumenta a amplitude de movimento e mobiliza a parte central interna dos peitorais. A extensão da posição inicial (permitindo que as mãos avancem até um plano acima dos ombros ou até a altura da cabeça) possibilita maior alongamento, mas também implica esforço desnecessário na articulação do ombro.</p>

