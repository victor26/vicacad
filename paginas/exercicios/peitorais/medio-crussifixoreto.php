<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Peitoral maior.
Secundários: Deltóide anterior, tríceps.</pre>

<img src="../imagens/peitorais/crucifixo-reto-crossover.jpg"/>

<h2>ESPAÇAMENTO DAS MÃOS</h2>
<p>
A orientação dos pegadores afeta a posição das mãos. O crucifixo funciona melhor quando os pegadores são mantidos com uma pegada neutra, mas uma pegada pronada também pode ser utilizada, como variação.</p>

