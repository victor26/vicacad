<h1>CRUCIFIXO RETO CROSS OVER</h1>



<h2>EXECUÇÃO</h2>
Para executar o crucifixo reto cross over:<br>
<ol>
    <li>Deitado em um banco horizontal, comece com os pegadores diretamente acima do tórax médio, com as palmas das mãos voltadas para dentro.</l1>
        <li>Abaixe os pegadores com um amplo movimento de abertura (para fora), dobrando ligeiramente os cotovelos durante a descida dos pesos até o nível do tórax.</l1>
            <li>Levante os pegadores em um movimento simultâneo, fazendo arco ascendente, em retorno à posição vertical.</l1>
                </ol>
