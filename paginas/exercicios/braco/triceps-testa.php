<h1>TRÍCEPS TESTA</h1>



<h2>EXECUÇÃO</h2>
Para executar o Tríceps Testa:<br>
<ol>
    <li>Deitado em um banco horizontal, segure uma barra com os braços estendidos acima de seu peito; use pegada fechada, com o dorso das mãos voltado para cima, e com as mãos afastadas em aproximadamente 15 cm.</l1>
        <li>Flexione os cotovelos e abaixe a barra até tocar a testa.</l1>
            <li>Impulsione a barra para cima, até que ocorra extensão total dos cotovelos.</l1>
                </ol>
