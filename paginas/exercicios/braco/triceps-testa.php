<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Tríceps
Secundário: Peito, deltoide, antebraço
</pre>

<img src="../imagens/braco/triceps-testa.jpg"/>

<h2>ESPAÇAMENTO DAS MÃOS</h2>
<p>
Uma pegada aberta enfatiza a parte interna do tríceps (cabeça longa), enquanto a pegada fechada objetiva a parte externa do tríceps (cabeça lateral). Mantenha os cotovelos fechados e não permita que se abram para os lados.</p>

<h2>POSIÇÃO DO CORPO</h2>
<p>
Mantenha os cotovelos apontados para cima e os braços na vertical. Não abaixe a barra na direção do seu rosto ou do queixo, pois isso fará com que os cotovelos “caiam” e permitirá que os músculos deltoide e peitorais ajudem no movimento.</p>

<h2>TRAJETÓRIA</h2>
<p>
A posição vertical do braço alonga a cabeça interna (longa) do tríceps; portanto, esse exercício mobiliza esta seção do musculo. O abaixamento da barra além da testa, na direção do banco, gera maior alongamento na cabeça longa, favorecendo sua contração durante o movimento.</p>

<h2>PEGADA</h2>
<p>
Utilizando uma barra reta, o exercício pode ser executado com uma pegada com o dorso das mãos voltado para cima (pronada) ou para baixo (supinda). Utilizando uma barra EZ ou halteres, será preciso uma pegada neutra. A pegada com o dorso das mãos voltado para baixo enfatiza a parte externa (cabeça lateral) e a pegada neutra trabalha as três cabeças do tríceps.</p>

<h2>AMPLITUDE DE MOVIMENTO</h2>
<p>
Para isolar o tríceps, o movimento deverá ocorrer no cotovelo, e não no ombro.</p>



