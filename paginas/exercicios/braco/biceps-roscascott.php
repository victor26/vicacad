<h1>ROSCA SCOTT</h1>



<h2>EXECUÇÃO</h2>
Para executar a Rosca Scott:<br>
<ol>
    <li>Segure a barra usando uma pegada com o dorso das mãos voltado para baixo e na largura dos ombros, com os cotovelos repousando na almofada e braços retos, voltados para fora.</l1>
        <li>Tracione a barra na direção dos ombros, flexionando os cotovelos.</l1>
            <li>Retorne a barra à posição de braços estendidos.</l1>
                </ol>
<img src="../gifs/braco/roscascot.gif"/>
	
	

<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Bíceps
Secundários: Braquial, braquiorradial, antebraço.
</pre>

<img src="../imagens/braco/rosca-scott.jpg"/>

<h2>ESPAÇAMENTO DAS MÃOS</h2>
<p>
A pegada aberta concentra o esforço na parte interna do bíceps (cabeça curta), enquanto a pegada fechada trabalha a parte externa do bíceps (cabeça longa).</p>

<h2>TRAJETÓRIA</h2>
<p>
Dependendo do movimento do aparelho, uma almofada inclinada para o braço concentrará o esforço na parte inferior do bíceps, enquanto uma almofada horizontal plana enfatizará o pico na parte média do bíceps.</p>

<h2>PEGADA</h2>
<p>
Realizar o Rosca no aparelho com uma barra angulada é menos desgastante para a articulação do punho</p>

<h2>AMPLITUDE DE MOVIMENTO</h2>
<p>
O esforço se concentra no bíceps inferir durante a fase inicial do exercício, transferindo-se então para o bíceps médio (pico) durante o levantamento do peso.</p>

<h2>RESISTÊNCIA</h2>
<p>
Ao contrário das roscas diretas com halteres ou barra, em que a resistência varia durante o levantamento, o aparelho proporciona resistência uniforme ao longo de todo o movimento.</p>

