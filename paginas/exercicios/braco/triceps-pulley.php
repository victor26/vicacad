<h1>TRÍCEPS PULLEY</h1>



<h2>EXECUÇÃO</h2>
Para executar o Tríceps Pulley:<br>
<ol>
    <li>Faça a pegada com o dorso das mãos voltado para cima e na largura dos ombros em uma barra curta presa a uma polia alta.</l1>
        <li>Comece com a barra no nível do peito, cotovelos dobrados um pouco mais do que 90 graus.</l1>
            <li>Mantendo os braços estendidos, tracione a barra para baixo até que os cotovelos fiquem bloqueados.</l1>
                </ol>
