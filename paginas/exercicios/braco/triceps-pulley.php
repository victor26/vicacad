<h2>ESPAÇAMENTO DAS MÃOS</h2>
<p>
A pegada aberta concentra o esforço na parte interna do tríceps (cabeça longa) enquanto a pegada fechada concentra o esforço na parte externa do tríceps (cabeça lateral).</p>

<h2>POSIÇÃO DO CORPO</h2>
<p>
Em pé, ereto, com a coluna vertebral reta: está e a posição padrão. Se forem utilizados grandes pesos, a leve inclinação do torso para frente, com a cintura, proporcionará mais estabilidade.</p>

<h2>TRAJETÓRIA</h2>
<p>
Com os braços perpendicularmente ao chão, a parte externa do tríceps (cabeça lateral) contribui para o movimento. Se o exercício for executado com os braços elevados e paralelos ao chão, o esforço ficará concentrado na parte interna do tríceps (cabeça longa).</p>

<h2>PEGADA</h2>
<p>
Utilizando uma barra reta, uma pegada pronada enfatiza a parte externa do tríceps enquanto uma pegada supinada concentra o esforço na parte interna (cabeça longa). Uma barra angulada em forma de V faz com que as mãos assumam uma pegada neutra, trabalhando igualmente todas as três cabeças do tríceps.</p>

<h2>AMPLITUDE DE MOVIMENTO</h2>
<p>
A fixação dos braços contra os lados do corpo impede o movimento no ombro, sendo um modo excelente de isolar o tríceps. O movimento deve ocorrer apenas no cotovelo.</p>

<h2>RESISTÊNCIA</h2>
<p>
Ao contrário dos exercícios com barra ou halter, em que a resistência varia durante o levantamento, o cabo proporciona resistência uniforme ao longo de todo o movimento.</p>


