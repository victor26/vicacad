<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Bíceps
Secundário: Braquial, braquiorradial, deltoide anterior, antebraço.
</pre>

<img src="../imagens/braco/rosca-direta.jpg"/>

<h2>ESPAÇAMENTO DAS MÃOS</h2>
<p>
A pegada mais aberta concentra o esforço na região interna do bíceps (cabeça curta), enquanto a pegada fechada trabalha a parte externa do bíceps (cabeça longa).</p>

<h2>TRAJETÓRIA</h2>
<p>
Na Rosca direta com barra, a barra deve se mover para cima e para baixo em um arco próximo ao corpo. Para isolar o bíceps, o movimento deve ocorrer no cotovelo e não no ombro.</p>

<h2>PEGADA</h2>
<p>
Com uma barra reta, a pegada com o dorso das mãos voltado para baixo fica fixada em supinação (palmas das mãos voltadas para cima). A pegada pode ser ajustada se for utilizada uma barra EZ ou barra W (ver seção de variação).</p>

<h2>AMPLITUDE DE MOVIMENTO</h2>
<p>
A interrupção do movimento alguns graus antes da completa extensão dos cotovelos mantém a tensão no bíceps durante a descida da barra..</p>

<h2>POSIÇÃO DO CORPO</h2>
<p>
Em pé, corpo ereto, com a coluna vertebral reta. Frequentemente usa-se a inclinação do torso como método de “trapaça” no lançamento do peso para cima com o uso do momento. Uma leve inclinação para frente facilita a fase inicial da flexão de braços. Uma leve inclinação para trás ajuda a completar a fase final da repetição.</p>

