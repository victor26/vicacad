<h1>ROSCA DIRETA</h1>



<h2>EXECUÇÃO</h2>
Para executar a Rosca Direta:<br>
<ol>
    <li>Segure a barra com os braços estendidos; pegada com afastamento igual à distancia entre os ombros e com o dorso das mãos voltado para baixo.</l1>
        <li>Leve a barra até o nível dos ombros; para tanto, flexione os cotovelos.</l1>
            <li>Abaixe a barra de volta à posição inicial, com os braços na posição estendida.</l1>
                </ol>
