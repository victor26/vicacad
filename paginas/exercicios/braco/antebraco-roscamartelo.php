<h1>ROSCA MARTELO</h1>



<h2>EXECUÇÃO</h2>
Para executar a Rosca Martelo:<br>
<ol>
    <li>Segure um halter fixo em cada mão com as palmas voltadas para dentro (polegares apontando para frente).</l1>
        <li>Levante um halter de cada vez até o ombro, mantendo as palmas das mãos voltadas para dentro.</l1>
            <li>Abaixe o halter de volta à posição de braço estendido e repita com o outro braço.</l1>
                </ol>
<img src="../gifs/braco/roscamartelo.gif"/>
	
	

<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Braquiorradial
Secundário: Braquial, extensores e flexores do antebraço, bíceps.</pre>

<img src="../imagens/braco/rosca-martelo.jpg"/>

<h2>PEGADA</h2>
<p>
O esforço do deltóide lateral fica maximizado quando os halteres são mantidos paralelamente ao chão. A inclinação dos halteres com os polegares voltados para cima promove a rotação externa do ombro, fazendo com que o deltóide anterior contribua para o movimento, enquanto a inclinação dos halteres com os polegares voltados para baixo promove a rotação interna do ombro, permitindo a ajuda do deltóide posterior.</p>

<h2>TRAJETÓRIA</h2>
<p>
Para concentrar o esforço no braquiorradial, levante o halter transversalmente ao seu corpo, e não ao lado dele.</p>

<h2>AMPLITUDE DE MOVIMENTO</h2>
<p>
Para maximizar o esforço do antebraço no Rosca martelo, trabalhe o pulso no plano vertical, empinando o polegar para cima durante o levantamento do halter.</p>


