<h1>ROSCA CONCENTRADA</h1>



<h2>EXECUÇÃO</h2>
Para executar a Rosca Concentrada:<br>
<ol>
    <li>Posição sentada na extremidade do banco. Segure o halter fixo com o braço estendido; apoie o braço contra a parte interna da coxa.</l1>
        <li>Faça o exercício de rosca com halter na direção do ombro, flexionando o cotovelo.</l1>
            <li>Abaixe o halter de volta a posição inicial.</l1>
                </ol>
