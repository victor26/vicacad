<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Bíceps
Secundário: Braquial, braquiorradial, músculos do antebraço.
</pre>

<img src="../imagens/braco/rosca-concentrada.jpg"/>

<h2>POSIÇÃO DO CORPO</h2>
<p>
Ao executar a Rosca Concentrada , o torso deve permanece imóvel, apoiado pela mão livre sobre a coxa oposta.</p>

<h2>TRAJETÓRIA</h2>
<p>
A posição do braço propriamente dito (com relação ao chão) muda o enfoque do esforço. Quando o braço está vertical (ombro diretamente acima do cotovelo), a resistência aumenta com a elevação do halter e o esforço se concentra na parte superior do bíceps (pico). Com o braço em um ângulo inclinado (cotovelo a frente do ombro), a resistência no inicio do exercício é máxima. Assim, o efeito é direcionado para a seção inferior do bíceps no cotovelo.</p>

<h2>PEGADA</h2>
<p>
A pegada com o dorso das mãos voltado para baixo posicione a mão em supinação; assim, maximiza a contração do bíceps.</p>

<h2>AMPLITUDE DE MOVIMENTO</h2>
<p>
O repouso do braço contra a coxa impede o movimento no ombro, sendo um modo excelente de isolar o bíceps.</p>


