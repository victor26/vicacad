<h1>ROSCA INVERTIDA</h1>



<h2>EXECUÇÃO</h2>
Para executar a Rosca Invertida:<br>
<ol>
    <li>Segure a barra do halter ou polia com os braços estendidos; use uma pegada com o dorso das mãos voltados para cima e com afastamento igual à distância entre os ombros.</l1>
        <li>Eleve a barra até o nível dos ombros, rosqueando os punhos para cima e para trás enquanto flexiona os cotovelos.</l1>
            <li>Abaixe a barra ou polia até a posição de braços estendidos, deixando que os punhos “caiam”.</l1>
                </ol>
