<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Extensores do antebraço, extensores dos dedos.
Secundário: Bíceps, braquiorradial, braquial.</pre>

<img src="../imagens/braco/rosca-invertida.jpg"/>

<h2>ESPAÇAMENTO DAS MÃOS</h2>
<p>
O espaçamento ideal das mãos é a largura dos ombros. As mãos devem estar diretamente alinhadas com os antebraços</p>

<h2>PEGADA</h2>
<p>
A rosca invertida exige uma pegada com o dorso das mãos voltado para cima (pronada), com as palmas da mãos voltadas para baixo e os polegares pegando em torno da barra.</p>

<h2>AMPLITUDE DE MOVIMENTO</h2>
<p>
Para maximizar o envolvimento dos antebraços, certifique-se de ter percorrido uma amplitude de movimento completa no punho. Empine o punho para trás até obter extensão completa durante o levantamento da barra, e flexiona o punho para baixo para descer o peso.</p>

<h2>RESISTÊNCIA</h2>
<p>
Devido à gravidade, a resistência aumenta com a elevação da barra. Para garantir máximo esforço no antebraço, atrase a rosca (extensão) do punho até que os antebraços estejam paralelos ao chão.</p>

