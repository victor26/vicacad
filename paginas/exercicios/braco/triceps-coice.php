<h1>TRÍCEPS COICE</h1>



<h2>EXECUÇÃO</h2>
Para executar o Tríceps Coice:<br>
<ol>
    <li>Pegue o halter com uma mão, encurve-se para frente (use a cintura), e sustente o torso pousando a mão livre em um banco, ou no joelho.</l1>
        <li>Comece com o braço paralelo ao chão e com o cotovelo dobrado em 90 graus.</l1>
            <li>Movimente para cima o halter, estendendo o braço até que ocorra total extensão do cotovelo.</l1>
                </ol>
<img src="../gifs/braco/tricepscoice.gif"/>
	
	

<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Tríceps
Secundário: Deltoide posterior, latíssimo do dorso.
</pre>

<img src="../imagens/braco/triceps-coice.jpg"/>

<h2>POSIÇÃO DO CORPO</h2>
<p>
O torso deve ficar em um plano ligeiramente acima do paralelo ao chão. Se você ficar demasiadamente ereto, não poderá executar com eficiência o exercício.</p>

<h2>TRAJETÓRIA</h2>
<p>
No tríceps coice, mantenha o braço paralelo ao chão e o cotovelo junto ao lado do corpo.</p>

<h2>PEGADA</h2>
<p>
A pegada neutra (polegar apontando para cima) trabalha todas as seções do tríceps. A rotação do halter, de modo que a palma da mão fique voltada para cima, mobiliza a parte externa (cabeça lateral) do tríceps.</p>

<h2>AMPLITUDE DE MOVIMENTO</h2>
<p>
Para isolar o tríceps, o movimento deve ocorrer no cotovelo e o ombro deve permanecer imóvel.</p>

<h2>RESISTÊNCIA</h2>
<p>
Devido ao efeito da gravidade, a resistência é variável, aumentado à medida que o halter é movimentado para cima.</p>


