<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Deltóide lateral
Secundário: Deltóide anterior, deltóide posterior, trapézio, supra-espinal.</pre>

<img src="../imagens/ombros/elevação-lateral-com-halter.jpg"/>

<h2>TRAJETÓRIA</h2>
<p>
O levantamento dos halteres diretamente para fora (ao lado do corpo) mobiliza o deltóide lateral. O levantamento dos halteres a partir da posição à frente dos quadris com um arco direcionado para a frente faz com que o deltóide anterior participe, ajudando no movimento. Se o arco de movimento ocorrer atrás do plano do corpo, então o deltóide posterior contribuirá para o levantamento.</p>

<h2>AMPLITUDE DE MOVIMENTO</h2>
<p>
O deltóide lateral executa a maior parte do trabalho quando os halteres são levantados até o nível do ombro. O trapézio assumirá o trabalho se os halteres forem levantados até um nível mais alto; portanto, o término da fase de levantamento no nível dos ombros mantém a tensão no deltóide.</p>

<h2>RESISTÊNCIA</h2>
<p>
Por causa do efeito da gravidade nos halteres, a resistência é menor no início do movimento, aumentando gradualmente até se tornar máxima, quando os halteres tiverem sido levantados até o nível dos ombros.</p>

<h2>PEGADA</h2>
<p>
O esforço do deltóide lateral fica maximizado quando os halteres são mantidos paralelamente ao chão. A inclinação dos halteres com os polegares voltados para cima promove a rotação externa do ombro, fazendo com que o deltóide anterior contribua para o movimento, enquanto a inclinação dos halteres com os polegares voltados para baixo promove a rotação interna do ombro, permitindo a ajuda do deltóide posterior.</p>

