<h1>ELEVAÇÃO LATERAL COM HALTERES </h1>



<h2>EXECUÇÃO</h2>
Para executar a Elevação Lateral com Halteres:<br>
<ol>
    <li>Na posição em pé ereta, segure os halters com os braços estendidos.</l1>
        <li>Levante os braços para fora e para os lados do corpo, até que os halteres atinjam o nível dos ombros.</l1>
            <li>Abaixe os halteres de volta para os quadris.</l1>
                </ol>
