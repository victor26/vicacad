<h1>LEVANTAMENTO FRONTAL COM CABO</h1>



<h2>EXECUÇÃO</h2>
Para executar o Levantamento Frontal com Cabo:<br>
<ol>
        <li>Com uma das mãos, segure o pegador preso a uma polia baixa, utilizando uma pegada pronada (palma das mãos para baixo).</l1>
            <li>Virado de costas para a pilha de pesos, levante o cabo em um arco ascendente até o nível do ombro, mantendo o cotovelo rígido.</l1>
           <li>Abaixe o cabo de volta até o nível da cintura.</li> 
</ol>
<img src="../gifs/ombros/levantamentofrontalcabo.gif"/>
	
	

<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Deltóide anterior.
Secundário: Deltóide lateral, trapézio, peitoral superior.
</pre>

<img src="../imagens/ombros/levantamento-frontal-com-cabo.jpg"/>

<h2>PEGADA</h2>
<p>
A pegada pronada com o dorso das mãos voltado para cima trabalha as cabeças anterior e lateral do deltóide.</p>

