<h1>LEVANTAMENTO FRONTAL COM CABO</h1>



<h2>EXECUÇÃO</h2>
Para executar o Desenvolvimento com Halter:<br>
<ol>
        <li>Sentado em um banco, comece com os halteres fixos no nível do ombro, palmas das mãos voltadas para frente.</l1>
            <li>Impulsione verticalmente para cima os halteres, até que ocorra bloqueio dos cotovelos.</l1>
           <li>Abaixe os halteres de volta, até tocarem os ombros.</li> 
</ol>
<img src="../gifs/ombros/desenvolvimentohalter.gif"/>
	
	

<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Deltóide Anterior
Secundários: Deltóide Latera, tríceps, trapézio, peitoral superior.
</pre>

<img src="../imagens/ombros/desenvolvimento-com-halter.jpg"/>

<h2>PEGADA</h2>
A mudança da orientação dos halteres fixos afeta a pegada.<br>
<ul>
        <li>O desenvolvimento dos halteres para cima com as palmas das mãos voltadas para a frente (pegada pronada) trabalhando tanto a cabeça anterior como a cabeça lateral da deltóide.</l1>
            <li>O desenvolvimento dos halteres com as palmas das mãos voltadas para dentro (pegada neutra) faz com que o deltóide anterior trabalhe de forma mais puxada, minimizando o envolvimento da cabeça lateral.</l1>
           <li>Se os halteres forem seguros com as palmas das mãos voltadas para trás (pegada supinada), isso maximizará o esforço do deltóide anterior.</li> 
</ul>

<h2>POSICIONAMENTO</h2>
<p>
A realização do exercício na posição sentada com as costas eretas é uma versão mais completa do que na posição em pé e impede que ocorra trapaça na elevação do peso para cima com o uso do momento.</p>

