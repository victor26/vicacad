<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Deltóide posterior
Secundário: Deltóide lateral, trapézio, rombóides, infra-espinal, redondo menor, redondo maior.</pre>

<img src="../imagens/ombros/cruzamento-de-cabos-com-inversão.jpg"/>

<h2>TRAJETÓRIA</h2>
<p>
Para mobilizar o deltóide posterior, os braços devem se mover diretamente para trás (e ligeiramente para baixo), quase paralelamente ao chão.  Se as mãos forem levantadas em um arco mais alto, até um ponto acima do nível do ombro, o trapézio e o deltóide lateral darão maior contribuição para o movimento.</p>

<h2>POSIÇÃO DO CORPO</h2>
<p>
O deltóide posterior será enfatizado com maior eficiência com o torso ereto, sem que haja inclinação excessiva para a frente ou para trás.</p>

<h2>AMPLITUDE DE MOVIMENTO</h2>
<p>
O cruzamento entre as mãos (descruzamento dos cabos ) na posição inicial aumenta a amplitude de movimento e o alongamento muscular, isso faz com que o deltóide posterior trabalhe mais intensamente.</p>

