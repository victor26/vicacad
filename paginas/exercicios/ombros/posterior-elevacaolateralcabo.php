<h1>ELEVAÇÃO LATERAL INCLINADO COM CABO </h1>



<h2>EXECUÇÃO</h2>
Para executar a Elevação Lateral Inclinado com Cabos:<br>
<ol>
    <li>Com o pegador esquerdo na mão direita, e o direito na mão esquerda, fique em pé no meio, e em seguida, incline o corpo para a frente usando a cintura, com as costas retas e paralelas ao chão.</l1>
        <li>Levante as mãos para cima em um arco até o nível dos ombros, de tal modeo que os cabos se cruzem.</l1>
            <li>Abaixe os pegadores até a posição inicial, com a mão direita diretamente à frente do tornozelo esquerdo e a mão esquerda diretamente à frente do tornozelo esquerdo.</l1>
                </ol>
<img src="../gifs/ombros/elevacaolateralcabo.gif"/>
	
	

<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Deltóide posterior
Secundário: Deltóide lateral, trapézio, rombóides, infra-espinal, redondo menor, redondo maior.</pre>

<img src="../imagens/ombros/elevação-lateral-com-cabo.jpg"/>

<h2>TRAJETÓRIA</h2>
<p>
Pra enfatizar o deltóide posterior, os braços devem se movimentar diretamente para fora, afastando-se dos lados do corpo. Se as mãos forem levantadas em um arco anterior na frente da sua cabeça, o trapézio e o deltóide lateral contribuirão para o exercício.</p>

<h2>POSIÇÃO DO CORPO</h2>
<p>
O isolamento do deltóide posterior será melhor com o torso paralelo ao chão, e não inclinado com o tórax e a cabeça em posição superior.</p>

<h2>AMPLITUDE DE MOVIMENTO</h2>
<p>
A amplitude de movimento no início do exercício aumentará se você permitir que as mãos se cruzem (com o descruzamento dos cabos) durante o abaixamento dos pegadores. O aumento da distância e o maior alongamento fazem com que o deltóide posterior trabalhe mais intensamente.</p>

<h2>RESISTÊNCIA</h2>
<p>
Ao contrário dos levantamentos com halteres, em que a resistência varia durante o levantamento, a polia de cabo permite resistência uniforme ao longo de todo o movimento.</p>

<h2>PEGADA</h2>
<p>
O pegador do cabo não permite que sejam feitas mudanças na posição ou pegada das mãos.</p>

