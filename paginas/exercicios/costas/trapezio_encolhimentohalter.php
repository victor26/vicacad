<h1>ENCOLHIMENTO COM HALTER</h1>



<h2>EXECUÇÃO</h2>
Para executar o encolhimento com halter:<br>
<ol>
    <li>Em pé, em uma posição ereta com um halter em cada mão, mãos pendentes aos lados do corpo.</l1>
        <li>Mantendo os braços estendidos, encolha os ombros para cima – até o ponho mais alto possível.</l1>
            <li>Abaixe os halteres de volta para a posição inicial.</l1>
                </ol>
<img src="../gifs/costas/encolhimentohalter.gif"/>
	
	

<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Trapézio (fibras superiores e médias)
Secundário: Levantador da escápula, deltóide, eretor da espinha, antebraços.</pre>

<img src="../imagens/costas/encolhimento-com-halter.jpg"/>

<h2>PEGADA</h2>
<p>
No encolhimento com halter, uma pegada neutra (polegares apontando para a frente) enfatiza o trapézio superior no pescoço, enquanto uma pegada pronada visa o trapézio médio nas costas.</p>

<h2>POSIÇÃO DO CORPO</h2>
<p>
A ligeira inclinação do torso para trás (na cintura) mobiliza o trapézio superior, enquanto a ligeira inclinação para a frente mobiliza o músculo em uma parte mais baixa, no pescoço. A execução do encolhimento de ombros na posição em pé bem vertical mobiliza as seções superior e média do músculo do trapézio.</p>

<h2>PEGADA</h2>
<p>
Quanto mais elevado for o peso, maior será o trabalho do trapézio. Quanto mais longe forem abaixados os halteres, maior será o alongamento ao final do exercício.</p>

