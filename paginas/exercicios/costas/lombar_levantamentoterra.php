<h1>LEVANTAMENTO TERRA</h1>



<h2>EXECUÇÃO</h2>
Para executar o Levantamento Terra:<br>
<ol>
    <li>Faça uma pegada com afastamento igual à largura dos ombros e com o dorso das mãos voltado para cima; braços estendidos e na posição agachada, dobrando joelhos e quadris</l1>
        <li>Mantendo a coluna vertebral reta e os cotovelos bloqueados, fique de pé, ereto, levantando a barra até o nível dos quadris.</l1>
            <li>Lentamente, abaixe a barra até o chão.</l1>
                </ol>
<img src="../gifs/costas/levantamentoterra.gif"/>
	
	

<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Eretor da espinha, glúteos, músculos posteriores da coxa.
Secundário: Trapézio, latíssimo do dorso, quadríceps, antebraços.
</pre>

<img src="../imagens/costas/levantamento-terra.jpg"/>

<h2>ESPAÇAMENTO DAS MÃOS</h2>
<p>
As mãos devem ficar com espaçamento igual à largura dos ombros, de modo que os braços fiquem pendendo verticalmente e as mãos se movimentem ao longo da parte externa das coxas.</p>

