<h1>EXTENSÃO LOMBAR</h1>



<h2>EXECUÇÃO</h2>
Para executar a Extensão do Lombar:<br>
<ol>
    <li>Fique deitado com o rosto voltado para o chão, com os quadris apoiados no banco e tornozelos fixados sob as almofadas.</l1>
        <li>Comece com o torso pendendo para baixo, com flexão de 90 graus na cintura</l1>
            <li>Eleve o corpo até que o torso esteja um pouco acima da posição de paralelismo com o chão.</l1>
                </ol>
<img src="../gifs/costas/extensãolombar.gif"/>
	
	

<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Eretos da espinha
Secundário: Latíssimo do dorso, glúteos, músculos posteriores da coxa.
</pre>

<img src="../imagens/costas/extensão-lombar.jpg"/>

<h2>POSIÇÕES DAS MÃOS</h2>
<p>
As mãos podem estar entrelaçadas atrás da região lombar, ou dobradas no peito.</p>

<h2>AMPLITUDE DE MOVIMENTO</h2>
<p>
O torso deve se movimentar para cima e para baixo, descrevendo um arco de aproximadamente 90 graus. Evite fazer hiperextensão da coluna vertebral. Os músculos eretores funcionam na estabilização e retificação da coluna vertebral, enquanto os glúteos e os músculos posteriores da coxa geram extensão do quadril durante esse movimento.</p>

