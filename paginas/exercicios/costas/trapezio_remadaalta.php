<h1>REMADA ALTA COM BARRA</h1>



<h2>EXECUÇÃO</h2>
Para executar a remada alta com barra:<br>
<ol>
    <li>Segure a barra com afastamento igual à distância entre os ombros; use uma pegada com o dorso das mãos voltado para cima. </l1>
        <li>Tracione a barra verticalmente para cima até chegar ao queixo; eleve o máximo possível os cotovelos. </l1>
            <li>Abaixe a barra lentamente, até que os braços fiquem na posição estendida </l1>
                </ol>
<img src="../gifs/costas/remadaalta.gif"/>
	
	

<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Latíssimo do dorso.

Secundário: Trapézio, rombóides, deltóide posterior, eretor da espinha, bíceps.</pre>

<img src="../imagens/costas/remada-alta-com-barra.jpg"/>

<h2>ESPAÇAMENTO DAS MÃOS</h2>
<p>
Na remada alta com barra:<br>
Uma pegada na barra com afastamento igual à distância entre ombros, ou mais fechada, enfatiza o trapézio.<br> 
Uma pegada mais aberta também trabalha o deltóide.</p>

