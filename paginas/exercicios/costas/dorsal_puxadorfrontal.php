<h1>PUXADOR FRONTAL</h1>



<h2>EXECUÇÃO</h2>
Para executar o Puxador Frontal Aberto:<br>
<ol>
        <li>Faça uma pegada na barra com o dorso das mãos voltado para cima; as mãos devem ficar a uma distância 15cm maior que a largura dos ombros.</l1>
            <li>Tracione a barra para baixo, até a parte superior do peito, contraindo os latíssimos.</l1>
           <li>Retorne a barra à posição inicial, acima da cabeça.</li> 
</ol>
<img src="../gifs/costas/puxadorfrontal.gif"/>
	
	

<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Latíssimo do dorso (seção externa)
Secundário: Deltóide posterior, trapézio inferior, rombóides
</pre>

<img src="../imagens/costas/puxador-frontal.jpg"/>

<h2>ESPAÇAMENTO DAS MÃOS</h2>
<p>
À medida que aumenta o espaçamento das mãos, o enfoque se transfere para a seção mais externa do latíssimo do dorso, sob a axila. Esta parte do músculo faz com que as costas fiquem largas.</p>

