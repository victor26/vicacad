<h2>ESPAÇAMENTO DAS MÃOS</h2>
<p>
Na remada baixa sentado, um maior espaçamento das mãos mobilizará a parte externa do trapézio, enquanto um posicionamento mais próximo das mãos enfocará a parte interna do trapézio.</p>

