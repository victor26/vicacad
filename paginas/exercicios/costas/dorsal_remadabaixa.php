<h1>REMADA BAIXA SENTADO</h1>



<h2>EXECUÇÃO</h2>
Para executar a remada baixa sentado:<br>
<ol>
   Para executar o Puxador Frontal Aberto:
        <li>Segure os pegadores presos aos cabos com os braços estendidos à frente.</l1>
            <li>Tracione os pegadores superiormente, na direção do peito, mantendo a coluna vertebral reta</l1>
           <li>Retorne os pegadores à posição inicial.</li> 
</ol>
