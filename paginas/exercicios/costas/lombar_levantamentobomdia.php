<h1>LEVANTAMENTO BOM DIA</h1>



<h2>EXECUÇÃO</h2>
Para executar o Levantamento Bom Dia:<br>
<ol>
    <li>Fique em pé em uma posição ereta com uma barra sob os ombros.</l1>
        <li>Mantendo a coluna vertebral reta e os joelhos rígidos (estendidos ou ligeiramente dobrados), incline-se para a frente (use a cintura) até que o torso esteja um pouco acima da posição paralela com relação ao chão.</l1>
            <li>Levante o torso de volta à posição ereta.</l1>
                </ol>
<img src="../gifs/costas/levantamentobomdia.gif"/>
	
	

<h2>MÚSCULOS ENVOLVIDOS</h2>	
<pre>Primário: Eretor da espinha
Secundário: Latíssimo do dorso, glúteos, músculos posteriores da coxa.
</pre>

<img src="../imagens/costas/levantamento-bom-dia.jpg"/>

<h2>ESPAÇAMENTO DAS MÃOS</h2>
<p>
Coloque as mãos um pouco mais afastadas do que a distância entre os ombros; use uma pegada com o dorso das mãos voltado para cima e segure a barra sobre os ombros.</p>

<h2>AMPLITUDE DE MOVIMENTO</h2>
<p>
No levantamento bom dia, o torso deve se movimentar para cima e para baixo percorrendo um arco de aproximadamente 90 graus. Mantenha a coluna vertebral reta e a cabeça para cima, e evite encurvar o torso até o nível abaixo do plano paralelo ao chão. Durante esse movimento, os músculos eretores da espinha trabalham para estabilizar e retificar a coluna vertebral, enquanto os músculos glúteos e músculos posteriores da coxa geram extensão dos quadris.</p>

