<h1>BARRA FIXA</h1>



<h2>EXECUÇÃO</h2>
<p>Flexões de braço na barra são semelhantes ao puxador, exceto que a resistência é proporcionada pelo peso do próprio corpo. Basicamente, flexões na barra utilizam a adução do ombro e, portanto, tendem a trabalhar os latíssimos externos, resultando em costas mais largas. Tente não “gangorrar” ao subir.</p>
